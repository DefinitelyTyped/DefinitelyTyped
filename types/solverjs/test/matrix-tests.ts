import solverjs = require('solverjs');

solverjs.matAdd(
    [
        [1, 2],
        [4, 5],
    ],
    [
        [6, 7],
        [8, 9],
    ],
);
solverjs.matSub(
    [
        [1, 2],
        [4, 5],
    ],
    [
        [6, 7],
        [8, 9],
    ],
);
solverjs.matSpiralPrint([
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
]);
solverjs.matTrans([
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
]);
