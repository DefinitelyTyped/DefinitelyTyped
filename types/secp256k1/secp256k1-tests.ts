import { SignOptions, ecdsaSign } from "secp256k1";

const opts: SignOptions = {
    data: Uint8Array.from([0x62, 0x75, 0x66, 0x66, 0x65, 0x72])
};

const message = Uint8Array.from([0x62, 0x75, 0x66, 0x66, 0x65, 0x72]);

const prvKey = Uint8Array.from([0x62, 0x75, 0x66, 0x66, 0x65, 0x72]);

ecdsaSign(message, prvKey, opts);
