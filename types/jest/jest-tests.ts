/* Basic matchers */

describe('', () => {
    it('', () => {
        expect(BigInt(0)).toBeGreaterThan(BigInt(1));

        expect(BigInt(0)).toBeGreaterThanOrEqual(BigInt(1));

        expect(BigInt(0)).toBeLessThan(BigInt(1));

        expect(BigInt(0)).toBeLessThanOrEqual(BigInt(1));
    });
});
